**Satchels:** These are purchasable items with which you search the sands around you. 1/3 of the bags can be purchased at every station, 1/3 can only be bought from top
hats / achieved from quests. And the other third can only be rare events. No bag can ever be purchased with Robux and the only things that can be purchased with Robux 
is cosmetics for the bags, or on some occasions special items.

**Catching With Satchels:** The primary mechanic in the game is catching dune fish in the sands around you. This can only be done with bags. Some stations will have 
better luck for different fish than other stations and caps will provide extra luck on fishing within 50 studs of them. 
