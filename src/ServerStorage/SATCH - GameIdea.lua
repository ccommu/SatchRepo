**Shop** - Allows you to purchase various different bags to catch dune fish in, however better bags will require extra effort, as the purchasable ones available at 
every station (what stations are is tbd) can only get so good. The others require completing quests + having specific items / quantities of an item.

**Special Sand** - Special sand can be purchased only from the fallen hat type of glasses. The glasses are the most common fallen hat type, 
however the special sand can also be purchased at all stations. The special sand allows you to attract better dune fish, however only has a limited quality so you 
can’t use it forever. All dune sand is purchasable, however it must be achieved from a stolen sand crate, which is what you purchase, from there it’s RNG on what 
sands you get and how much. Some fish are exclusive to some special sands.

**Dropped Hats** - Dropped hats are randomly generated hats that spawn around the map, they will have a notification when one spawns, however the location 
will be merely with a marker showing either where it is or which direction it’s in (at the top of your screen with a direction meter) depending on how close you are. 
If you’re close enough it will show a marker that showcases where the random hat is, however most of the time it will be on the angle meter at the top of your screen 
with the respective color and name in the angle direction of the object. The top hats are the rarest, and the only place you can get a certain stolen sand shipment 
container, needed for a specific extremely rare fish. Along with that it will feed into the storyline in some form (TBD) and will be a hangout / safe space from the 
outer winds during storms. It’ll also have a special shop allowing you to directly purchase certain sands, or purchase otherwise non purchasable bags. The cap is the
second rarest dropped hat, it doesn’t spawn as much as glasses, but not as rarely as top hats. The cap will sell slightly better sand crates than the glasses and stations,
and will also provide higher luck when searching the sand around it, the cap will also be the gateway to the first and most beginner quest, only requiring you to be level
15 and have the third bag in the game or higher. Glasses are the most common dropped hat, selling the basic special sand crate, but also being a partial shelter against 
the occasional sand storm, where instead a cave, cap, or top hats would be a better fit.

**Sand Storms** - Sand storms are the occasionally occurring event, that triggers stronger sand particles, and also slows down your movement speed when going against
it with a custom animation making you hold your hand in front of your eyes, the sandstorm also severely lowers visibility, and will slowly make you take damage unless 
you’re in a cap, cave, or top hat. If you’re in glasses, you will take 2-5 times less damage depending on the type of glasses (shades, goggles, etc.) If you’re going In 
the direction of the sand storm you will move slightly like ice and also pushing you forward with the wind, you will have a custom animation making your character seem 
like it’s being pushed by the wind.

